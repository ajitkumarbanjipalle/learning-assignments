package operator;

public class RelationalOperator {
    public static void main(String[] args) {

        // > , >= , < , <= , == , !=
        // return of relational operator always a boolean value

        int i = 10;
        int j = 10;

        System.out.println(i > j);
        System.out.println(i >= j);
        System.out.println(i < j);
        System.out.println(i <= j);
        System.out.println(i == j);
        System.out.println(i != j);
    }
}
