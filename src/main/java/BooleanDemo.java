public class BooleanDemo {
    public static void main(String[] args) {

        boolean b1 = true;
        boolean b2 = false;

        // boolean is only datatype
        // whose value is keyword

        System.out.println(b1);
        System.out.println(b2);

        boolean a1 = true;
        boolean a2 = false;

        System.out.println(a1);
        System.out.println(a2);
    }
}
